public class constructor {
   void java(){
       System.out.println("java");
   }
   void python(){
       System.out.println("python");
   }
   void c(){
       System.out.println("c");
   }
   void Kotlin(){
       System.out.println("Kotlin");
   }

    public static void main(String[] args) {
        constructor acti = new constructor();
        acti.java();
        acti.python();
        acti.c();
        acti.Kotlin();
    }
}
